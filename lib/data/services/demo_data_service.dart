  Future<void> seedPragueToNaples() async {
    const sessionId = 'demo-prague-naples';

    final existing = await (db.select(db.sessions)
          ..where((t) => t.id.equals(sessionId)))
        .getSingleOrNull();
    if (existing != null) return;

    final startTime = DateTime.now().subtract(const Duration(hours: 5));

    await db.into(db.sessions).insert(
      SessionsCompanion.insert(
        id: sessionId,
        type: 'flight',
        title: 'Demo: Prague → Naples (Straight)',
        fromName: const Value('Prague (PRG)'),
        toName: const Value('Naples (NAP)'),
        fromLat: const Value(50.1008),
        fromLon: const Value(14.2567),
        toLat: const Value(40.8844),
        toLon: const Value(14.2908),
        startedAt: startTime,
        endedAt: Value(startTime.add(const Duration(hours: 2))),
        durationSec: const Value(7200),
        distanceKm: const Value(1030.0),
        sourceType: 'demo',
        notes: const Value('Old straight-line demo flight for replay testing.'),
      ),
    );

    final points = <TrackPointsCompanion>[];

    const startLat = 50.1008;
    const startLon = 14.2567;
    const endLat = 40.8844;
    const endLon = 14.2908;

    for (int i = 0; i <= 240; i++) {
      final t = i / 240.0;

      final lat = startLat + (endLat - startLat) * t;
      final lon = startLon + (endLon - startLon) * t;

      double alt = 0;
      if (t < 0.2) {
        alt = (t / 0.2) * 10000;
      } else if (t > 0.8) {
        alt = ((1.0 - t) / 0.2) * 10000;
      } else {
        alt = 10000 + (sin(t * 50) * 50);
      }

      double speed = 850.0;
      if (t < 0.1) speed = (t / 0.1) * 850;
      if (t > 0.9) speed = ((1.0 - t) / 0.1) * 850;

      points.add(
        TrackPointsCompanion.insert(
          sessionId: sessionId,
          timestamp: startTime.add(Duration(seconds: i * 30)),
          lat: lat,
          lon: lon,
          altMeters: alt,
          speedKmh: speed,
          headingDeg: 195.0,
          phase: Value(t < 0.2 ? 'climb' : (t > 0.8 ? 'descent' : 'cruise')),
        ),
      );
    }

    await db.batch((batch) {
      batch.insertAll(db.trackPoints, points);
    });
  }

  Future<void> seedPragueToNaplesRealTrack() async {
    const sessionId = 'demo-prague-naples-real';

    final existing = await (db.select(db.sessions)
          ..where((t) => t.id.equals(sessionId)))
        .getSingleOrNull();
    if (existing != null) return;

    final rawPoints = _parseEmbeddedTrack(_realTrackCoords);
    if (rawPoints.length < 2) return;

    const totalDurationSec = 5198;
    const fromName = 'Prague (PRG)';
    const toName = 'Naples (NAP)';

    final startTime = DateTime.parse('2026-04-06T13:42:41Z');

    double totalDistanceKm = 0.0;
    for (int i = 1; i < rawPoints.length; i++) {
      totalDistanceKm += _haversineKm(
        rawPoints[i - 1].lat,
        rawPoints[i - 1].lon,
        rawPoints[i].lat,
        rawPoints[i].lon,
      );
    }

    await db.into(db.sessions).insert(
      SessionsCompanion.insert(
        id: sessionId,
        type: 'flight',
        title: 'Demo: Prague → Naples (Real Track)',
        fromName: const Value(fromName),
        toName: const Value(toName),
        fromLat: Value(rawPoints.first.lat),
        fromLon: Value(rawPoints.first.lon),
        toLat: Value(rawPoints.last.lat),
        toLon: Value(rawPoints.last.lon),
        startedAt: startTime,
        endedAt: Value(startTime.add(const Duration(seconds: totalDurationSec))),
        durationSec: const Value(totalDurationSec),
        distanceKm: Value(totalDistanceKm),
        sourceType: 'demo',
        notes: const Value(
          'FlightAware-based real track demo for replay testing.',
        ),
      ),
    );

    final points = <TrackPointsCompanion>[];

    for (int i = 0; i < rawPoints.length; i++) {
      final point = rawPoints[i];
      final timestampOffsetSec =
          ((totalDurationSec * i) / (rawPoints.length - 1)).round();

      final heading = i < rawPoints.length - 1
          ? _bearingDeg(
              point.lat,
              point.lon,
              rawPoints[i + 1].lat,
              rawPoints[i + 1].lon,
            )
          : (rawPoints.length > 1
                ? _bearingDeg(
                    rawPoints[i - 1].lat,
                    rawPoints[i - 1].lon,
                    point.lat,
                    point.lon,
                  )
                : 0.0);

      double speed = 0.0;
      if (i > 0) {
        final prev = rawPoints[i - 1];
        final prevOffsetSec =
            ((totalDurationSec * (i - 1)) / (rawPoints.length - 1)).round();
        final dtSec = max(1, timestampOffsetSec - prevOffsetSec);
        final distanceKm = _haversineKm(
          prev.lat,
          prev.lon,
          point.lat,
          point.lon,
        );
        speed = (distanceKm / dtSec) * 3600.0;
      }

      final phase = _phaseFor(
        index: i,
        total: rawPoints.length,
        altitude: point.alt,
      );

      points.add(
        TrackPointsCompanion.insert(
          sessionId: sessionId,
          timestamp: startTime.add(Duration(seconds: timestampOffsetSec)),
          lat: point.lat,
          lon: point.lon,
          altMeters: point.alt,
          speedKmh: speed,
          headingDeg: heading,
          phase: Value(phase),
        ),
      );
    }

    await db.batch((batch) {
      batch.insertAll(db.trackPoints, points);
    });
  }

  List<_RealTrackPoint> _parseEmbeddedTrack(String raw) {
    final points = <_RealTrackPoint>[];

    for (final line in raw.trim().split('\n')) {
      final cleaned = line.trim();
      if (cleaned.isEmpty) continue;

      final parts = cleaned.split(RegExp(r'\s+'));
      if (parts.length < 3) continue;

      final lon = double.tryParse(parts[0]);
      final lat = double.tryParse(parts[1]);
      final alt = double.tryParse(parts[2]);

      if (lat != null && lon != null && alt != null) {
        points.add(_RealTrackPoint(lat: lat, lon: lon, alt: alt));
      }
    }

    return points;
  }

  double _haversineKm(double lat1, double lon1, double lat2, double lon2) {
    const r = 6371.0088;
    final p1 = lat1 * pi / 180.0;
    final p2 = lat2 * pi / 180.0;
    final dp = (lat2 - lat1) * pi / 180.0;
    final dl = (lon2 - lon1) * pi / 180.0;

    final a =
        sin(dp / 2) * sin(dp / 2) +
        cos(p1) * cos(p2) * sin(dl / 2) * sin(dl / 2);

    return 2 * r * asin(sqrt(a));
  }

  double _bearingDeg(double lat1, double lon1, double lat2, double lon2) {
    final p1 = lat1 * pi / 180.0;
    final p2 = lat2 * pi / 180.0;
    final dl = (lon2 - lon1) * pi / 180.0;

    final y = sin(dl) * cos(p2);
    final x = cos(p1) * sin(p2) - sin(p1) * cos(p2) * cos(dl);
    final brng = atan2(y, x) * 180.0 / pi;

    return (brng + 360.0) % 360.0;
  }

  String _phaseFor({
    required int index,
    required int total,
    required double altitude,
  }) {
    final t = total <= 1 ? 0.0 : index / (total - 1);

    if (t < 0.12 || altitude < 2500) return 'climb';
    if (t > 0.82 || altitude < 3500) return 'descent';
    return 'cruise';
  }
}

